      PROGRAM DATEST
      INTEGER I,J,IOS

      OPEN (12,IOSTAT=IOS,FILE='TESTFILE',RECL=6,ACCESS='DIRECT',
     1FORM='FORMATTED',ERR=90)
      DO 100 I=1,9
      WRITE (12,"I6",REC=I) I*111111
100   CONTINUE
      CLOSE (12)

      OPEN (12,IOSTAT=IOS,FILE='TESTFILE',ACCESS='DIRECT',RECL=6,
     1STATUS='OLD',FORM='FORMATTED',ERR=90)
      DO 200 I=9,1,-1
      READ (12,"I6",REC=I) J
      PRINT *,J
200   CONTINUE
      CLOSE (12,STATUS='DELETE')
      STOP
90    PRINT *,'ERROR IN OPEN: ',IOS
      STOP
      END
      