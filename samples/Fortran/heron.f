      PROGRAM TRIANGLE
    
C AREA OF A TRIANGLE - HERON'S FORMULA
C INPUT - CARD READER UNIT 5, INTEGER INPUT, NO BLANK CARD FOR END OF DATA
C OUTPUT - LINE PRINTER UNIT 6, REAL OUTPUT
C INPUT ERROR DISPAYS ERROR MESSAGE ON OUTPUT

  501 FORMAT(3I)
  601 FORMAT(1X,"A= ",I5,"  B= ",I5,"  C= ",I5,"  AREA= ",F10.2,
     1" SQUARE UNITS")
  602 FORMAT(1X,"NORMAL END")
  603 FORMAT(1X,"INPUT ERROR OR ZER
     1O VALUE ERROR")
      INTEGER A,B,C
      REAL S
   10 READ(*,501,END=50,ERR=90) A,B,C
      IF(A=0 .OR. B=0 .OR. C=0) GO TO 90
      S = (A + B + C) / 2.0
      AREA = SQRT(S * (S - A) * (S - B) * (S - C))
      WRITE(*,601) A,B,C,AREA
      GO TO 10
   50 WRITE(*,602)
      STOP
   90 WRITE(*,603)
      STOP
      END
